// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let copy = [];
  for (index = array.length - 1; index >= 0; index--){
    copy.push(array[index])
  }
  return copy
}

