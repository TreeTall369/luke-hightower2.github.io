////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {

  if (x > y){
    return y
  } else if (y > x){
    return x
  } else {
    return 0
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {

if (number % 2 === 0){
  return true
} else {
  return false
}

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
let array = [];
for (index = 0; index < string.length; index++){
  if (string[index] === letter){
    array.push(index)
  }
}
return array.length
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let array = []
  for (index = 0; index < string.length; index++){
    if(string[index] === 'B'){
      array.push(index)
    }
  }
return array.length

}

