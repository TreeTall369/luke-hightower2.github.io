function dominantDirection(string) {
// create two arrays, one ltr, other called rtl
let ltr = [];
let rtl = [];

for (let i = 0; i < string.length; i ++){
  let script = characterScript(string.charCodeAt(i))
  if (script !== null){
    if(script.direction === 'ltr'){
      ltr.push(script)
    } else {
      rtl.push(script)
    }
  }

}

if (ltr.length > rtl.length){
  return 'ltr'
} else {
  return 'rtl'
}


}

